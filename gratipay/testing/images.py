from contextlib import contextmanager
from tempfile import NamedTemporaryFile


@contextmanager
def path_to(extension):
    fp = NamedTemporaryFile(prefix='gratipay-', suffix='.'+extension, delete=False)
    fp.write(ORIGINAL)
    fp.close()
    yield fp.name
    os.unlink(fp.name)


